public class MediaSystem {

  public static void main(String[] args) {
    String acronym = "M";

    if (acronym == "S")
      System.out.println("SMALL");
    else if (acronym == "M")
      System.out.println("MEDIUM");
    else if (acronym == "B")
      System.out.println("BIG");
    else
      System.out.println("UNDEFINED");
  }
}